/**
 * BUILD-17 Vercel-safe preview delivery generator.
 *
 * Reads the TilePyramid_PL01 export manifest (produced by export:all:static),
 * copies the exported HTML files into delivery/preview/ (NOT delivery/latest/),
 * and generates a delivery-manifest.json compatible with build-preview.mjs.
 *
 * Writing to delivery/preview/ keeps delivery/latest/ (the real delivery package)
 * untouched, so validate:delivery continues to work independently.
 *
 * No Playwright, no Chromium. Safe for Vercel's build environment.
 *
 * Usage: node apps/internal-preview/scripts/generate-preview-delivery.mjs
 * Root shortcut: npm run vercel:generate-delivery
 */

import { copyFile, mkdir, readFile, rm, stat, writeFile } from 'node:fs/promises';
import { createHash } from 'node:crypto';
import { execFile } from 'node:child_process';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const APP_ROOT = path.join(HERE, '..');
const REPO_ROOT = path.join(APP_ROOT, '..', '..');
const TP_ROOT = path.join(REPO_ROOT, 'projects/TilePyramid_PL01');
const EXPORT_ROOT = path.join(TP_ROOT, 'exports/latest');
// NOTE: delivery/preview/ — separate from delivery/latest/ (the real delivery package)
const DELIVERY_ROOT = path.join(TP_ROOT, 'delivery/preview');

// ─── helpers ──────────────────────────────────────────────────────────────────

async function sha256File(filePath) {
  const buffer = await readFile(filePath);
  return createHash('sha256').update(buffer).digest('hex');
}

async function getGitBranch() {
  return new Promise(resolve => {
    execFile('git', ['branch', '--show-current'], { cwd: REPO_ROOT }, (error, stdout) => {
      resolve(error ? null : stdout.trim() || null);
    });
  });
}

// ─── main ─────────────────────────────────────────────────────────────────────

async function main() {
  console.log('\nVercel preview delivery generator — BUILD-17');
  console.log('─'.repeat(50));
  console.log('Output: delivery/preview/ (delivery/latest/ is NOT touched)');

  // Load export manifest (generated by export:all:static)
  const exportManifestPath = path.join(EXPORT_ROOT, 'export-manifest.json');
  let exportManifest;
  try {
    exportManifest = JSON.parse(await readFile(exportManifestPath, 'utf8'));
  } catch {
    console.error(`ERROR: Export manifest not found at ${exportManifestPath}`);
    console.error('  Run first: cd projects/TilePyramid_PL01 && npm run export:all:static');
    process.exit(1);
  }

  const unityEntry = exportManifest.networks?.find(n => n.network === 'unity');
  const applovinEntry = exportManifest.networks?.find(n => n.network === 'applovin');
  if (!unityEntry || !applovinEntry) {
    console.error('ERROR: Export manifest missing unity or applovin network entries');
    process.exit(1);
  }

  if (unityEntry.status !== 'PASS' || applovinEntry.status !== 'PASS') {
    console.error(
      `ERROR: Export status not PASS: unity=${unityEntry.status} applovin=${applovinEntry.status}`
    );
    process.exit(1);
  }

  // Prepare delivery/preview/ (never touches delivery/latest/)
  await rm(DELIVERY_ROOT, { recursive: true, force: true });
  await mkdir(path.join(DELIVERY_ROOT, 'unity'), { recursive: true });
  await mkdir(path.join(DELIVERY_ROOT, 'applovin'), { recursive: true });

  // Copy exported HTML files (outputPath in manifest is relative to TP_ROOT)
  const unitySrc = path.join(TP_ROOT, unityEntry.outputPath);
  const unityRelDst = 'unity/TilePyramid_PL01_unity.html';
  const unityDst = path.join(DELIVERY_ROOT, unityRelDst);
  await copyFile(unitySrc, unityDst);

  const applovinSrc = path.join(TP_ROOT, applovinEntry.outputPath);
  const applovinRelDst = 'applovin/TilePyramid_PL01_applovin.html';
  const applovinDst = path.join(DELIVERY_ROOT, applovinRelDst);
  await copyFile(applovinSrc, applovinDst);

  console.log(`Copied Unity HTML    → delivery/preview/${unityRelDst}`);
  console.log(`Copied AppLovin HTML → delivery/preview/${applovinRelDst}`);

  // Compute checksums and sizes
  const [unitySha, applovinSha, unitySt, applovinSt] = await Promise.all([
    sha256File(unityDst),
    sha256File(applovinDst),
    stat(unityDst),
    stat(applovinDst),
  ]);

  const gitBranch = await getGitBranch();
  const storeUrls = unityEntry.storeUrls ?? {};

  // Write delivery-manifest.json compatible with build-preview.mjs
  const manifest = {
    projectId: 'TilePyramid_PL01',
    build: 'BUILD-17-vercel',
    deliveryType: 'vercel-preview',
    generatedAt: new Date().toISOString(),
    gitBranch,
    outputRoot: 'delivery/preview',
    storeUrls: {
      androidUrl: storeUrls.androidUrl ?? '',
      iosUrl: storeUrls.iosUrl ?? '',
      fallbackUrl: storeUrls.fallbackUrl ?? storeUrls.androidUrl ?? '',
    },
    outputs: {
      unity: {
        network: 'unity',
        path: unityRelDst,
        sizeBytes: unitySt.size,
        sha256: unitySha,
        targetMaxBytes: unityEntry.targetMaxBytes ?? 5 * 1024 * 1024,
        profileId: unityEntry.profileId,
      },
      applovin: {
        network: 'applovin',
        path: applovinRelDst,
        sizeBytes: applovinSt.size,
        sha256: applovinSha,
        targetMaxBytes: applovinEntry.targetMaxBytes ?? 5 * 1024 * 1024,
        profileId: applovinEntry.profileId,
      },
    },
    networkQaEvidence: {
      unity: {
        status: 'PASSED_UPLOAD_TESTING',
        fixedIn: 'BUILD-12',
        fix: 'window.top removal from Phaser input manager and build output sanitization',
        disclaimer:
          'Passed current Unity upload/testing after BUILD-12 window.top fix. Not guaranteed forever.',
      },
      applovin: {
        status: 'PASSED_UPLOAD_TESTING',
        fixedIn: 'BUILD-12',
        fix: 'window.top removal from Phaser input manager and build output sanitization',
        disclaimer:
          'Passed current AppLovin upload/testing after BUILD-12 window.top fix. Not guaranteed forever.',
      },
    },
    formalSolvability: 'NOT YET PROVEN',
    knownLimitations: [
      'Final Unity Ads and AppLovin approval is not guaranteed.',
      'Unity export expects network-provided window.mraid.',
      'Network webviews may differ from Chromium file:// QA.',
      'Formal solvability remains NOT YET PROVEN.',
      'Visual smoke tests not run in Vercel build (no Playwright).',
    ],
    deliveryDisclaimer:
      'Generated in Vercel build (static validation only). Visual smoke tests require local Playwright run.',
    exportSource: exportManifest.generatedAt,
  };

  await writeFile(
    path.join(DELIVERY_ROOT, 'delivery-manifest.json'),
    JSON.stringify(manifest, null, 2)
  );

  console.log('\nPreview delivery manifest written to delivery/preview/:');
  console.log(`  Unity:    ${manifest.outputs.unity.path}  ${manifest.outputs.unity.sizeBytes} bytes`);
  console.log(`  AppLovin: ${manifest.outputs.applovin.path}  ${manifest.outputs.applovin.sizeBytes} bytes`);
  console.log(`  Unity SHA256:    ${manifest.outputs.unity.sha256}`);
  console.log(`  AppLovin SHA256: ${manifest.outputs.applovin.sha256}`);
  console.log(`  Store URLs: Android=${manifest.storeUrls.androidUrl ? 'OK' : 'MISSING'} iOS=${manifest.storeUrls.iosUrl ? 'OK' : 'MISSING'}`);
  console.log('  delivery/latest/ was NOT touched.');
}

main().catch(err => {
  console.error('Preview delivery generation failed:', err.message);
  process.exit(1);
});

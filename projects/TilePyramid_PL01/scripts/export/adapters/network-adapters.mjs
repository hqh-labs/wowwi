export function createNetworkExportMetadata(profile, validation, inlinedAssets, storeUrls = {}) {
  return {
    network: profile.network,
    profileId: profile.id,
    displayName: profile.displayName,
    outputType: profile.outputType,
    targetMaxBytes: profile.targetMaxBytes,
    actualBytes: validation.actualBytes,
    status: validation.status,
    warnings: validation.warnings,
    errors: validation.errors,
    mraidRequired: profile.mraidRequired,
    mraidBootstrapAllowed: profile.mraidBootstrapAllowed,
    networkProvidedMraid: profile.networkProvidedMraid,
    requiresNoExternalResources: profile.requiresNoExternalResources,
    orientationPolicy: profile.orientationPolicy,
    timerFirstInteractionRequired: profile.timerFirstInteractionRequired,
    storeOpenBehavior: profile.storeOpenBehavior,
    hostCloseButtonSafeZone: profile.hostCloseButtonSafeZone,
    safeAreaPolicy: profile.safeAreaPolicy,
    domOverlayPolicy: profile.domOverlayPolicy,
    formalSolvability: 'NOT YET PROVEN',
    knownLimitations: [
      'Final Unity Ads/AppLovin upload acceptance is not guaranteed by local validation.',
      'Unity export expects network-provided window.mraid when running in the ad container.',
      'Formal solvability remains NOT YET PROVEN.',
      'Chromium file:// QA may differ from final network webviews.',
    ],
    finalApprovalDisclaimer: profile.finalApprovalDisclaimer,
    finalApprovalGuaranteed: false,
    storeUrls: {
      androidUrl: storeUrls.androidUrl ?? '',
      iosUrl: storeUrls.iosUrl ?? '',
      fallbackUrl: storeUrls.fallbackUrl ?? '',
    },
    inlinedAssetCount: inlinedAssets.length,
    inlinedAssetBytes: inlinedAssets.reduce((total, asset) => total + asset.bytes, 0),
  };
}
